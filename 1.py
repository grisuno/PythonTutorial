# Variables
# Una variable es un espacio de memoria donde almacenamos datos.
# Python es un lenguaje de tipado dinámico, lo que significa que no necesitamos especificar
# el tipo de variable al declararla.

# Asignación de variables
mensaje = "¡Bienvenido a Python!"
numero_entero = 10
numero_flotante = 3.14
booleano = True

# Imprimir variables
print(mensaje)  # Esto imprimirá el valor de la variable mensaje.
print(numero_entero)  # Esto imprimirá el valor de la variable numero_entero.
print(numero_flotante)  # Esto imprimirá el valor de la variable numero_flotante.
print(booleano)  # Esto imprimirá el valor de la variable booleano.

# Verificar el tipo de variable
print(type(mensaje))  # Esto imprimirá <class 'str'>
print(type(numero_entero))  # Esto imprimirá <class 'int'>
print(type(numero_flotante))  # Esto imprimirá <class 'float'>
print(type(booleano))  # Esto imprimirá <class 'bool'>

